#import "KCViewController.h"
#import "KCView.h"
#import "UIScreen+KCServices.h"
@interface KCViewController ()

@end

@implementation KCViewController

////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - ViewController lifecycle
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (void)loadView
{
    self.view = [[[self viewClass] alloc] initWithFrame:[self viewFrame] delegate:self];
}

- (void)viewDidLoad
{
    [super viewDidLoad];
}

- (void)viewWillAppear:(BOOL)animated
{
    [(KCView *)self.view viewWillAppear];
}

- (void)viewDidAppear:(BOOL)animated
{
    [(KCView *)self.view viewDidAppear];
}

- (void)viewWillDisappear:(BOOL)animated
{
    [(KCView *)self.view viewWillDisappear];
}

- (void)viewDidDisappear:(BOOL)animated
{
    [(KCView *)self.view viewDidDisappear];
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark - Convenience
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////
- (Class)viewClass
{
    return [KCView class];
}

- (CGRect)viewFrame
{
    return [[UIScreen mainScreen] applicationFrame];
}

- (NSUInteger)supportedInterfaceOrientations
{
    return UIInterfaceOrientationPortrait;
}



@end
